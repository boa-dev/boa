macro_rules! well_known_statics {
    ( $( $(#[$attr:meta])* ($name:ident, $string:literal) ),+$(,)? ) => {
        $(
            paste!{
                #[doc = "Gets the static `JsString` for `\"" $string "\"`."]
                pub const $name: JsString = const {
                    JsString::from_static_js_str(Self::find_static_js_string($string))
                };
            }
        )+
    };
}

